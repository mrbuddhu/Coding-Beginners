// C++ program to demonstrate the 
// sinh() function 
#include <bits/stdc++.h> 
using namespace std; 

int main() 
{ 
	double x = 4.1; 

	double result = sinh(x); 
	cout << "sinh(4.1) = " << result << endl; 

	// x in Degrees 
	double xDegrees = 90; 
	x = xDegrees * 3.14159 / 180; 

	result = sinh(x); 
	cout << "sinh(90 degrees) = " << result << endl; 

	return 0; 
} 
